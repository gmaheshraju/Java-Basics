package lesson1;

import java.util.Scanner;

public class PatternPro {
	public static void main(String[] args) {
		Scanner sc= new Scanner(System.in);
		int n =sc.nextInt();
		//1
	    // 2 3
		// 4 5 6
		
		for(int i=1;i<=n;i++){
			System.out.print(i); 
			
			System.out.println();
			
		
		}
		
	}

}
